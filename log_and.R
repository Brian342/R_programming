# logarithms and exponentials
# when supplied a given number x and a value referred to as base, the logarithm calculates
# the power to which you must raise the base to get to x
# eg logarithm of x = 243 to base 3
# log(x=243, base = 3)
# Things to consider 
# both x and the base must be positive, 
# The log of any number x when the base is equal to x is 1
# the log of x = 1 is always 0, regardless of the base

