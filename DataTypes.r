# Data types examples may include 
# - numeric types, 10.5, 7, 845
# - character values
# - boolean values/ Logical values eg (TRUE, FALSE)
# - Complex Types eg 2+5!
# - Raw 'hello' is stored as 48 65 6c 6c 6f

# Numeric data type
my_obj <- 123
class(my_obj)
print(class)

# character data type
my_obj <- "Briaaaaaaan"
print(class(my_obj))

# logical data types
my_obj <- TRUE
print(class(my_obj))

# complex data types
my_obj <- 2 + 5i
print(class(my_obj))

x <- 100 # double
typeof(x)

y <- 10.5
typeof(y)

# integer
v <- 2L
print(class(v))

